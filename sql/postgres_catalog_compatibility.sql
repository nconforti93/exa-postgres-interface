CREATE OR REPLACE VIEW INFORMATION_SCHEMA.COLUMNS AS
SELECT
    'exasol' AS "TABLE_CATALOG",
    COLUMN_SCHEMA AS "TABLE_SCHEMA",
    COLUMN_TABLE AS "TABLE_NAME",
    COLUMN_NAME AS "COLUMN_NAME",
    COLUMN_ORDINAL_POSITION AS "ORDINAL_POSITION",
    COLUMN_DEFAULT AS "COLUMN_DEFAULT",
    CASE WHEN COLUMN_IS_NULLABLE THEN 'YES' ELSE 'NO' END AS "IS_NULLABLE",
    CASE
        WHEN COLUMN_TYPE LIKE 'DECIMAL(%' THEN 'numeric'
        WHEN COLUMN_TYPE LIKE 'VARCHAR(%' THEN 'character varying'
        WHEN COLUMN_TYPE LIKE 'CHAR(%' THEN 'character'
        WHEN COLUMN_TYPE LIKE 'TIMESTAMP%' THEN 'timestamp without time zone'
        WHEN COLUMN_TYPE = 'DATE' THEN 'date'
        WHEN COLUMN_TYPE = 'BOOLEAN' THEN 'boolean'
        WHEN COLUMN_TYPE IN ('DOUBLE', 'DOUBLE PRECISION') THEN 'double precision'
        WHEN COLUMN_TYPE = 'REAL' THEN 'real'
        ELSE 'text'
    END AS "DATA_TYPE",
    COLUMN_MAXSIZE AS "CHARACTER_MAXIMUM_LENGTH",
    COLUMN_MAXSIZE AS "CHARACTER_OCTET_LENGTH",
    COLUMN_NUM_PREC AS "NUMERIC_PRECISION",
    COLUMN_NUM_SCALE AS "NUMERIC_SCALE",
    CAST(10 AS DECIMAL(9,0)) AS "NUMERIC_PRECISION_RADIX",
    CAST(NULL AS DECIMAL(9,0)) AS "DATETIME_PRECISION",
    CAST(NULL AS VARCHAR(128)) AS "INTERVAL_TYPE",
    CAST(NULL AS DECIMAL(9,0)) AS "INTERVAL_PRECISION",
    CAST(NULL AS VARCHAR(128)) AS "CHARACTER_SET_CATALOG",
    CAST(NULL AS VARCHAR(128)) AS "CHARACTER_SET_SCHEMA",
    CAST(NULL AS VARCHAR(128)) AS "CHARACTER_SET_NAME",
    CAST(NULL AS VARCHAR(128)) AS "COLLATION_CATALOG",
    CAST(NULL AS VARCHAR(128)) AS "COLLATION_SCHEMA",
    CAST(NULL AS VARCHAR(128)) AS "COLLATION_NAME",
    'exasol' AS "DOMAIN_CATALOG",
    COLUMN_SCHEMA AS "DOMAIN_SCHEMA",
    CASE
        WHEN COLUMN_TYPE LIKE 'DECIMAL(%' THEN 'numeric'
        WHEN COLUMN_TYPE LIKE 'VARCHAR(%' THEN 'varchar'
        WHEN COLUMN_TYPE LIKE 'CHAR(%' THEN 'bpchar'
        WHEN COLUMN_TYPE LIKE 'TIMESTAMP%' THEN 'timestamp'
        WHEN COLUMN_TYPE = 'DATE' THEN 'date'
        WHEN COLUMN_TYPE = 'BOOLEAN' THEN 'bool'
        WHEN COLUMN_TYPE IN ('DOUBLE', 'DOUBLE PRECISION') THEN 'float8'
        WHEN COLUMN_TYPE = 'REAL' THEN 'float4'
        ELSE 'text'
    END AS "UDT_NAME",
    CAST(NULL AS VARCHAR(128)) AS "SCOPE_CATALOG",
    CAST(NULL AS VARCHAR(128)) AS "SCOPE_SCHEMA",
    CAST(NULL AS VARCHAR(128)) AS "SCOPE_NAME",
    CAST(NULL AS DECIMAL(9,0)) AS "MAXIMUM_CARDINALITY",
    CAST(NULL AS VARCHAR(128)) AS "DTD_IDENTIFIER",
    'NO' AS "IS_SELF_REFERENCING",
    'NO' AS "IS_IDENTITY",
    CAST(NULL AS VARCHAR(128)) AS "IDENTITY_GENERATION",
    CAST(NULL AS VARCHAR(128)) AS "IDENTITY_START",
    CAST(NULL AS VARCHAR(128)) AS "IDENTITY_INCREMENT",
    CAST(NULL AS VARCHAR(128)) AS "IDENTITY_MAXIMUM",
    CAST(NULL AS VARCHAR(128)) AS "IDENTITY_MINIMUM",
    CAST(NULL AS VARCHAR(128)) AS "IDENTITY_CYCLE",
    'NO' AS "IS_GENERATED",
    CAST(NULL AS VARCHAR(2000)) AS "GENERATION_EXPRESSION",
    CASE WHEN COLUMN_IDENTITY IS NULL THEN 'NO' ELSE 'YES' END AS "IS_UPDATABLE"
FROM SYS.EXA_DBA_COLUMNS
WHERE COLUMN_OBJECT_TYPE IN ('TABLE', 'VIEW')
  AND COLUMN_SCHEMA NOT IN ('SYS', 'PG_CATALOG', 'INFORMATION_SCHEMA');

