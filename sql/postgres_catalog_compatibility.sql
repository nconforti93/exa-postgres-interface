CREATE OR REPLACE VIEW PG_CATALOG.PG_AUTH_MEMBERS AS
WITH BASE AS (
    WITH ROLE_LOOKUP AS (
        SELECT OID, ROLNAME
        FROM PG_CATALOG.PG_ROLES
    )
    SELECT
        CAST(900000 + ROW_NUMBER() OVER (ORDER BY RP.GRANTED_ROLE, RP.GRANTEE) AS DECIMAL(18,0)) AS OID,
        COALESCE(ROLE_OID.OID, CAST(0 AS DECIMAL(18,0))) AS ROLEID,
        COALESCE(MEMBER_OID.OID, CAST(0 AS DECIMAL(18,0))) AS MEMBER,
        CAST(10 AS DECIMAL(18,0)) AS GRANTOR,
        COALESCE(RP.ADMIN_OPTION, FALSE) AS ADMIN_OPTION,
        TRUE AS INHERIT_OPTION,
        TRUE AS SET_OPTION
    FROM SYS.EXA_DBA_ROLE_PRIVS RP
    LEFT JOIN ROLE_LOOKUP ROLE_OID
      ON ROLE_OID.ROLNAME = RP.GRANTED_ROLE
    LEFT JOIN ROLE_LOOKUP MEMBER_OID
      ON MEMBER_OID.ROLNAME = RP.GRANTEE
)
SELECT
    BASE."OID" AS "OID",
    BASE."ROLEID" AS "ROLEID",
    BASE."MEMBER" AS "MEMBER",
    BASE."GRANTOR" AS "GRANTOR",
    BASE."ADMIN_OPTION" AS "ADMIN_OPTION",
    BASE."INHERIT_OPTION" AS "INHERIT_OPTION",
