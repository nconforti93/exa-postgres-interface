CREATE OR REPLACE VIEW PG_CATALOG.PG_USER AS
SELECT
    USER_NAME AS USENAME,
    CASE WHEN USER_NAME = 'SYS' THEN CAST(10 AS DECIMAL(18,0))
         ELSE CAST(10000 + ROW_NUMBER() OVER (ORDER BY USER_NAME) AS DECIMAL(18,0))
    END AS USESYSID,
    TRUE AS USECREATEDB,
    CASE WHEN USER_NAME = 'SYS' THEN TRUE ELSE FALSE END AS USESUPER,
    FALSE AS USEREPL,
    CASE WHEN USER_NAME = 'SYS' THEN TRUE ELSE FALSE END AS USEBYPASSRLS,
    CAST(NULL AS VARCHAR(64)) AS PASSWD,
    PASSWORD_EXPIRY AS VALUNTIL,
    CAST(NULL AS VARCHAR(2000)) AS USECONFIG
FROM SYS.EXA_DBA_USERS;

