CREATE OR REPLACE FUNCTION PG_CATALOG.TO_REGCLASS(
    CLASS_NAME VARCHAR(2000000)
)
RETURN DECIMAL(18,0)
IS
BEGIN
    RETURN CASE LOWER(REPLACE(CLASS_NAME, 'pg_catalog.', ''))
        WHEN 'pg_class' THEN 1259
        WHEN 'pg_database' THEN 1262
        WHEN 'pg_namespace' THEN 2615
        WHEN 'pg_proc' THEN 1255
        WHEN 'pg_type' THEN 1247
        ELSE 0
    END;
END TO_REGCLASS;
/

