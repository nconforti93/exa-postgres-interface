CREATE OR REPLACE VIEW PG_CATALOG.PG_NAMESPACE AS
WITH BASE AS (
    WITH ROLE_LOOKUP AS (
        SELECT OID, ROLNAME
        FROM PG_CATALOG.PG_ROLES
    )
    SELECT
        SCHEMA_OBJECT_ID AS OID,
        SCHEMA_NAME AS NSPNAME,
        COALESCE(R.OID, CAST(10 AS DECIMAL(18,0))) AS NSPOWNER,
        CAST(NULL AS VARCHAR(2000)) AS NSPACL
    FROM SYS.EXA_DBA_SCHEMAS
    LEFT JOIN ROLE_LOOKUP R
      ON R.ROLNAME = SCHEMA_OWNER
    WHERE SCHEMA_NAME NOT IN ('SYS', 'PG_CATALOG', 'INFORMATION_SCHEMA')
    UNION ALL
    SELECT CAST(11 AS DECIMAL(18,0)), 'PG_CATALOG', CAST(10 AS DECIMAL(18,0)), CAST(NULL AS VARCHAR(2000))
    UNION ALL
    SELECT CAST(12 AS DECIMAL(18,0)), 'INFORMATION_SCHEMA', CAST(10 AS DECIMAL(18,0)), CAST(NULL AS VARCHAR(2000))
)
SELECT
    BASE."OID" AS "OID",
    BASE."NSPNAME" AS "NSPNAME",
    BASE."NSPOWNER" AS "NSPOWNER",
    BASE."NSPACL" AS "NSPACL"
FROM BASE;

