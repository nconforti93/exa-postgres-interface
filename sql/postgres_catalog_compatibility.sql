    I.INDEX_TABLE || '_IDX_' || CAST(I.INDEX_OBJECT_ID AS VARCHAR(20)) AS RELNAME,
    N.OID AS RELNAMESPACE,
    CAST(0 AS DECIMAL(18,0)) AS RELTYPE,
