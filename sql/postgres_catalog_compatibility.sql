CREATE OR REPLACE VIEW PG_CATALOG.PG_CLASS AS
WITH TABLE_COLUMNS AS (
    SELECT COLUMN_SCHEMA, COLUMN_TABLE, COUNT(*) AS COLUMN_COUNT
    FROM SYS.EXA_DBA_COLUMNS
    WHERE COLUMN_OBJECT_TYPE = 'TABLE'
    GROUP BY COLUMN_SCHEMA, COLUMN_TABLE
),
VIEW_COLUMNS AS (
    SELECT COLUMN_SCHEMA, COLUMN_TABLE, COUNT(*) AS COLUMN_COUNT
    FROM SYS.EXA_DBA_COLUMNS
    WHERE COLUMN_OBJECT_TYPE = 'VIEW'
    GROUP BY COLUMN_SCHEMA, COLUMN_TABLE
),
TABLE_INDEXES AS (
    SELECT INDEX_SCHEMA, INDEX_TABLE, COUNT(*) AS INDEX_COUNT
    FROM SYS.EXA_DBA_INDICES
    GROUP BY INDEX_SCHEMA, INDEX_TABLE
),
ROLE_LOOKUP AS (
    SELECT OID, ROLNAME
    FROM PG_CATALOG.PG_ROLES
)
SELECT
    T.TABLE_OBJECT_ID AS OID,
    T.TABLE_NAME AS RELNAME,
    N.OID AS RELNAMESPACE,
    CAST(0 AS DECIMAL(18,0)) AS RELTYPE,
    CAST(0 AS DECIMAL(18,0)) AS RELOFTYPE,
    COALESCE(R.OID, CAST(10 AS DECIMAL(18,0))) AS RELOWNER,
    CAST(0 AS DECIMAL(18,0)) AS RELAM,
    CAST(0 AS DECIMAL(18,0)) AS RELFILENODE,
    CAST(0 AS DECIMAL(18,0)) AS RELTABLESPACE,
    CAST(0 AS DECIMAL(18,0)) AS RELPAGES,
    COALESCE(T.TABLE_ROW_COUNT, 0) AS RELTUPLES,
    CAST(0 AS DECIMAL(18,0)) AS RELALLVISIBLE,
    CAST(0 AS DECIMAL(18,0)) AS RELTOASTRELID,
    COALESCE(I.INDEX_COUNT, 0) > 0 AS RELHASINDEX,
    FALSE AS RELISSHARED,
    'p' AS RELPERSISTENCE,
    'r' AS RELKIND,
    COALESCE(C.COLUMN_COUNT, 0) AS RELNATTS,
    CAST(0 AS DECIMAL(18,0)) AS RELCHECKS,
    FALSE AS RELHASRULES,
    FALSE AS RELHASTRIGGERS,
    FALSE AS RELHASSUBCLASS,
    FALSE AS RELROWSECURITY,
    FALSE AS RELFORCEROWSECURITY,
    TRUE AS RELISPOPULATED,
    'n' AS RELREPLIDENT,
    FALSE AS RELISPARTITION,
    CAST(0 AS DECIMAL(18,0)) AS RELREWRITE,
    CAST(0 AS DECIMAL(18,0)) AS RELFROZENXID,
    CAST(0 AS DECIMAL(18,0)) AS RELMINMXID,
    CAST(NULL AS VARCHAR(2000)) AS RELACL,
    CAST(NULL AS VARCHAR(2000)) AS RELOPTIONS,
    CAST(NULL AS VARCHAR(2000)) AS RELPARTBOUND
FROM SYS.EXA_DBA_TABLES T
JOIN PG_CATALOG.PG_NAMESPACE N
  ON N.NSPNAME = T.TABLE_SCHEMA
LEFT JOIN ROLE_LOOKUP R
  ON R.ROLNAME = T.TABLE_OWNER
LEFT JOIN TABLE_COLUMNS C
  ON C.COLUMN_SCHEMA = T.TABLE_SCHEMA AND C.COLUMN_TABLE = T.TABLE_NAME
LEFT JOIN TABLE_INDEXES I
  ON I.INDEX_SCHEMA = T.TABLE_SCHEMA AND I.INDEX_TABLE = T.TABLE_NAME
WHERE T.TABLE_SCHEMA NOT IN ('SYS', 'PG_CATALOG', 'INFORMATION_SCHEMA')
UNION ALL
SELECT
    V.VIEW_OBJECT_ID AS OID,
    V.VIEW_NAME AS RELNAME,
    N.OID AS RELNAMESPACE,
    CAST(0 AS DECIMAL(18,0)) AS RELTYPE,
    CAST(0 AS DECIMAL(18,0)) AS RELOFTYPE,
    COALESCE(R.OID, CAST(10 AS DECIMAL(18,0))) AS RELOWNER,
    CAST(0 AS DECIMAL(18,0)) AS RELAM,
    CAST(0 AS DECIMAL(18,0)) AS RELFILENODE,
    CAST(0 AS DECIMAL(18,0)) AS RELTABLESPACE,
    CAST(0 AS DECIMAL(18,0)) AS RELPAGES,
    CAST(0 AS DECIMAL(18,0)) AS RELTUPLES,
    CAST(0 AS DECIMAL(18,0)) AS RELALLVISIBLE,
    CAST(0 AS DECIMAL(18,0)) AS RELTOASTRELID,
    FALSE AS RELHASINDEX,
    FALSE AS RELISSHARED,
    'p' AS RELPERSISTENCE,
    'v' AS RELKIND,
    COALESCE(C.COLUMN_COUNT, 0) AS RELNATTS,
    CAST(0 AS DECIMAL(18,0)) AS RELCHECKS,
    FALSE AS RELHASRULES,
    FALSE AS RELHASTRIGGERS,
    FALSE AS RELHASSUBCLASS,
    FALSE AS RELROWSECURITY,
    FALSE AS RELFORCEROWSECURITY,
    TRUE AS RELISPOPULATED,
    'n' AS RELREPLIDENT,
    FALSE AS RELISPARTITION,
    CAST(0 AS DECIMAL(18,0)) AS RELREWRITE,
    CAST(0 AS DECIMAL(18,0)) AS RELFROZENXID,
    CAST(0 AS DECIMAL(18,0)) AS RELMINMXID,
    CAST(NULL AS VARCHAR(2000)) AS RELACL,
    CAST(NULL AS VARCHAR(2000)) AS RELOPTIONS,
    CAST(NULL AS VARCHAR(2000)) AS RELPARTBOUND
FROM SYS.EXA_DBA_VIEWS V
JOIN PG_CATALOG.PG_NAMESPACE N
  ON N.NSPNAME = V.VIEW_SCHEMA
LEFT JOIN ROLE_LOOKUP R
  ON R.ROLNAME = V.VIEW_OWNER
LEFT JOIN VIEW_COLUMNS C
  ON C.COLUMN_SCHEMA = V.VIEW_SCHEMA AND C.COLUMN_TABLE = V.VIEW_NAME
WHERE V.VIEW_SCHEMA NOT IN ('SYS', 'PG_CATALOG', 'INFORMATION_SCHEMA');

CREATE OR REPLACE VIEW PG_CATALOG.PG_ATTRIBUTE AS
SELECT
    CLS.OID AS ATTRELID,
    COL.COLUMN_NAME AS ATTNAME,
    CASE
        WHEN COL.COLUMN_TYPE LIKE 'DECIMAL(%' THEN CAST(1700 AS DECIMAL(18,0))
        WHEN COL.COLUMN_TYPE LIKE 'VARCHAR(%' THEN CAST(1043 AS DECIMAL(18,0))
        WHEN COL.COLUMN_TYPE LIKE 'CHAR(%' THEN CAST(1042 AS DECIMAL(18,0))
        WHEN COL.COLUMN_TYPE LIKE 'TIMESTAMP%' THEN CAST(1114 AS DECIMAL(18,0))
        WHEN COL.COLUMN_TYPE = 'DATE' THEN CAST(1082 AS DECIMAL(18,0))
        WHEN COL.COLUMN_TYPE = 'BOOLEAN' THEN CAST(16 AS DECIMAL(18,0))
        WHEN COL.COLUMN_TYPE IN ('DOUBLE', 'DOUBLE PRECISION') THEN CAST(701 AS DECIMAL(18,0))
        WHEN COL.COLUMN_TYPE = 'REAL' THEN CAST(700 AS DECIMAL(18,0))
        ELSE CAST(25 AS DECIMAL(18,0))
    END AS ATTTYPID,
    CASE
        WHEN COL.COLUMN_TYPE LIKE 'TIMESTAMP%' THEN CAST(8 AS DECIMAL(9,0))
        WHEN COL.COLUMN_TYPE = 'DATE' THEN CAST(4 AS DECIMAL(9,0))
        WHEN COL.COLUMN_TYPE = 'BOOLEAN' THEN CAST(1 AS DECIMAL(9,0))
        WHEN COL.COLUMN_TYPE = 'REAL' THEN CAST(4 AS DECIMAL(9,0))
        WHEN COL.COLUMN_TYPE IN ('DOUBLE', 'DOUBLE PRECISION') THEN CAST(8 AS DECIMAL(9,0))
        ELSE CAST(-1 AS DECIMAL(9,0))
    END AS ATTLEN,
    COL.COLUMN_ORDINAL_POSITION AS ATTNUM,
    CASE
        WHEN COL.COLUMN_TYPE LIKE 'DECIMAL(%' THEN (COALESCE(COL.COLUMN_NUM_PREC, 36) * 65536) + COALESCE(COL.COLUMN_NUM_SCALE, 0) + 4
        WHEN COL.COLUMN_TYPE LIKE 'VARCHAR(%' THEN COALESCE(COL.COLUMN_MAXSIZE, 0) + 4
        WHEN COL.COLUMN_TYPE LIKE 'CHAR(%' THEN COALESCE(COL.COLUMN_MAXSIZE, 0) + 4
        WHEN COL.COLUMN_TYPE LIKE 'TIMESTAMP%' THEN 10
        ELSE -1
    END AS ATTTYPMOD,
    CAST(0 AS DECIMAL(9,0)) AS ATTNDIMS,
    FALSE AS ATTBYVAL,
    COALESCE(COL.COLUMN_IS_NULLABLE = FALSE, FALSE) AS ATTNOTNULL,
    CAST(NULL AS VARCHAR(1)) AS ATTIDENTITY,
    CAST(NULL AS VARCHAR(1)) AS ATTGENERATED,
    FALSE AS ATTISDROPPED
FROM SYS.EXA_DBA_COLUMNS COL
JOIN PG_CATALOG.PG_NAMESPACE NS
  ON NS.NSPNAME = COL.COLUMN_SCHEMA
JOIN PG_CATALOG.PG_CLASS CLS
  ON CLS.RELNAME = COL.COLUMN_TABLE
 AND CLS.RELNAMESPACE = NS.OID
WHERE COL.COLUMN_OBJECT_TYPE IN ('TABLE', 'VIEW');

