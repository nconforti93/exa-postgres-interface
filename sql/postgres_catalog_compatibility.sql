CREATE OR REPLACE VIEW PG_CATALOG.PG_ROLES AS
WITH ROLE_BASE AS (
    SELECT
        USER_NAME AS ROLE_NAME,
        TRUE AS CAN_LOGIN,
        CASE WHEN USER_NAME = 'SYS' THEN CAST(10 AS DECIMAL(18,0))
             ELSE CAST(10000 + ROW_NUMBER() OVER (ORDER BY USER_NAME) AS DECIMAL(18,0))
        END AS ROLE_OID,
        USER_COMMENT AS ROLE_COMMENT
    FROM SYS.EXA_DBA_USERS
    UNION ALL
    SELECT
        ROLE_NAME,
        FALSE AS CAN_LOGIN,
        CAST(20000 + ROW_NUMBER() OVER (ORDER BY ROLE_NAME) AS DECIMAL(18,0)) AS ROLE_OID,
        ROLE_COMMENT
    FROM SYS.EXA_DBA_ROLES
)
SELECT
    ROLE_OID AS OID,
    ROLE_NAME AS ROLNAME,
    CASE WHEN ROLE_NAME = 'SYS' THEN TRUE ELSE FALSE END AS ROLSUPER,
    TRUE AS ROLINHERIT,
    CASE WHEN ROLE_NAME = 'SYS' THEN TRUE ELSE FALSE END AS ROLCREATEROLE,
    CASE WHEN ROLE_NAME = 'SYS' THEN TRUE ELSE FALSE END AS ROLCREATEDB,
    CAN_LOGIN AS ROLCANLOGIN,
    FALSE AS ROLREPLICATION,
    CAST(-1 AS DECIMAL(9,0)) AS ROLCONNLIMIT,
    CAST(NULL AS VARCHAR(64)) AS ROLPASSWORD,
    CAST(NULL AS TIMESTAMP) AS ROLVALIDUNTIL,
    CASE WHEN ROLE_NAME = 'SYS' THEN TRUE ELSE FALSE END AS ROLBYPASSRLS,
    CAST(NULL AS VARCHAR(2000)) AS ROLCONFIG,
    ROLE_COMMENT AS ROLCOMMENT
FROM ROLE_BASE;

