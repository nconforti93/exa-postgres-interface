-- PostgreSQL catalog compatibility layer for Exasol.
--
-- This script creates two schemas, PG_CATALOG and INFORMATION_SCHEMA, and
-- populates them with Exasol-backed compatibility views for the core metadata
-- surfaces used by PostgreSQL clients. PostgreSQL-specific features that do
-- not have an Exasol equivalent are exposed as empty views with the expected
-- columns so clients receive empty result sets instead of object-not-found
-- errors.

CREATE SCHEMA IF NOT EXISTS PG_CATALOG;
CREATE SCHEMA IF NOT EXISTS INFORMATION_SCHEMA;

