CREATE OR REPLACE VIEW PG_CATALOG.PG_CONSTRAINT AS
WITH REF_TARGET AS (
    SELECT
        CONSTRAINT_SCHEMA,
        CONSTRAINT_TABLE,
        CONSTRAINT_NAME,
        MAX(REFERENCED_SCHEMA) AS REFERENCED_SCHEMA,
        MAX(REFERENCED_TABLE) AS REFERENCED_TABLE
    FROM SYS.EXA_DBA_CONSTRAINT_COLUMNS
    GROUP BY CONSTRAINT_SCHEMA, CONSTRAINT_TABLE, CONSTRAINT_NAME
),
CON_KEYS AS (
    SELECT
        CONSTRAINT_SCHEMA,
        CONSTRAINT_TABLE,
        CONSTRAINT_NAME,
        '{' || GROUP_CONCAT(CAST(ORDINAL_POSITION AS VARCHAR(12)) ORDER BY ORDINAL_POSITION SEPARATOR ',') || '}' AS CONKEY
    FROM SYS.EXA_DBA_CONSTRAINT_COLUMNS
    GROUP BY CONSTRAINT_SCHEMA, CONSTRAINT_TABLE, CONSTRAINT_NAME
),
REF_KEYS AS (
    SELECT
        C.CONSTRAINT_SCHEMA,
        C.CONSTRAINT_TABLE,
        C.CONSTRAINT_NAME,
        '{' || GROUP_CONCAT(CAST(RC.COLUMN_ORDINAL_POSITION AS VARCHAR(12)) ORDER BY C.ORDINAL_POSITION SEPARATOR ',') || '}' AS CONFKEY
    FROM SYS.EXA_DBA_CONSTRAINT_COLUMNS C
    JOIN SYS.EXA_DBA_COLUMNS RC
      ON RC.COLUMN_SCHEMA = C.REFERENCED_SCHEMA
     AND RC.COLUMN_TABLE = C.REFERENCED_TABLE
     AND RC.COLUMN_NAME = C.REFERENCED_COLUMN
     AND RC.COLUMN_OBJECT_TYPE = 'TABLE'
    WHERE C.CONSTRAINT_TYPE = 'FOREIGN KEY'
    GROUP BY C.CONSTRAINT_SCHEMA, C.CONSTRAINT_TABLE, C.CONSTRAINT_NAME
)
SELECT
    CAST(500000 + ROW_NUMBER() OVER (ORDER BY C.CONSTRAINT_SCHEMA, C.CONSTRAINT_TABLE, C.CONSTRAINT_NAME) AS DECIMAL(18,0)) AS OID,
    C.CONSTRAINT_NAME AS CONNAME,
    NS.OID AS CONNAMESPACE,
    CASE
        WHEN C.CONSTRAINT_TYPE = 'PRIMARY KEY' THEN 'p'
        WHEN C.CONSTRAINT_TYPE = 'FOREIGN KEY' THEN 'f'
        WHEN C.CONSTRAINT_TYPE = 'NOT NULL' THEN 'n'
        ELSE 'c'
    END AS CONTYPE,
    FALSE AS CONDEFERRABLE,
    FALSE AS CONDEFERRED,
    TRUE AS CONVALIDATED,
    CLS.OID AS CONRELID,
    CAST(0 AS DECIMAL(18,0)) AS CONTYPID,
    CAST(0 AS DECIMAL(18,0)) AS CONINDID,
    CAST(0 AS DECIMAL(18,0)) AS CONPARENTID,
    CAST(CASE WHEN C.CONSTRAINT_TYPE = 'FOREIGN KEY' THEN REFCLS.OID ELSE 0 END AS DECIMAL(18,0)) AS CONFRELID,
    CK.CONKEY AS CONKEY,
    RK.CONFKEY AS CONFKEY,
    'a' AS CONFUPDTYPE,
    'a' AS CONFDELTYPE,
    's' AS CONMATCHTYPE,
    TRUE AS CONISLOCAL,
    CAST(0 AS DECIMAL(9,0)) AS CONINHCOUNT,
    FALSE AS CONNOINHERIT,
    CAST(NULL AS VARCHAR(2000)) AS CONBIN
FROM SYS.EXA_DBA_CONSTRAINTS C
JOIN PG_CATALOG.PG_NAMESPACE NS
  ON NS.NSPNAME = C.CONSTRAINT_SCHEMA
JOIN PG_CATALOG.PG_CLASS CLS
  ON CLS.RELNAME = C.CONSTRAINT_TABLE
 AND CLS.RELNAMESPACE = NS.OID
LEFT JOIN REF_TARGET RT
  ON RT.CONSTRAINT_SCHEMA = C.CONSTRAINT_SCHEMA
 AND RT.CONSTRAINT_TABLE = C.CONSTRAINT_TABLE
 AND RT.CONSTRAINT_NAME = C.CONSTRAINT_NAME
LEFT JOIN PG_CATALOG.PG_NAMESPACE RNS
  ON RNS.NSPNAME = RT.REFERENCED_SCHEMA
LEFT JOIN PG_CATALOG.PG_CLASS REFCLS
  ON REFCLS.RELNAME = RT.REFERENCED_TABLE
 AND REFCLS.RELNAMESPACE = RNS.OID
LEFT JOIN CON_KEYS CK
  ON CK.CONSTRAINT_SCHEMA = C.CONSTRAINT_SCHEMA
 AND CK.CONSTRAINT_TABLE = C.CONSTRAINT_TABLE
 AND CK.CONSTRAINT_NAME = C.CONSTRAINT_NAME
LEFT JOIN REF_KEYS RK
  ON RK.CONSTRAINT_SCHEMA = C.CONSTRAINT_SCHEMA
 AND RK.CONSTRAINT_TABLE = C.CONSTRAINT_TABLE
 AND RK.CONSTRAINT_NAME = C.CONSTRAINT_NAME
WHERE C.CONSTRAINT_SCHEMA NOT IN ('SYS', 'PG_CATALOG', 'INFORMATION_SCHEMA');

