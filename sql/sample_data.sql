CREATE OR REPLACE FUNCTION DEMO_SHARED.OPEN_BALANCE (
    INVOICE_AMOUNT DECIMAL(18, 2),
    PAID_AMOUNT DECIMAL(18, 2)
)
RETURN DECIMAL(18, 2)
IS
BEGIN
    RETURN INVOICE_AMOUNT - COALESCE(PAID_AMOUNT, 0);
END OPEN_BALANCE;
