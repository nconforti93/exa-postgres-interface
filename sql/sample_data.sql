CREATE OR REPLACE VIEW DEMO_SALES.ORDER_FACT_V AS
SELECT
    O.ORDER_ID,
    O.ORDER_TS,
    O.ORDER_STATUS,
    C.CUSTOMER_CODE,
    C.CUSTOMER_NAME,
    C.REGION,
    O.GROSS_AMOUNT,
    O.NET_AMOUNT,
    O.CURRENCY_CODE,
    COUNT(I.ORDER_ITEM_ID) AS LINE_COUNT
FROM DEMO_SALES.ORDERS O
JOIN DEMO_SALES.CUSTOMERS C
  ON C.CUSTOMER_ID = O.CUSTOMER_ID
LEFT JOIN DEMO_SALES.ORDER_ITEMS I
  ON I.ORDER_ID = O.ORDER_ID
GROUP BY
    O.ORDER_ID,
    O.ORDER_TS,
    O.ORDER_STATUS,
    C.CUSTOMER_CODE,
    C.CUSTOMER_NAME,
    C.REGION,
    O.GROSS_AMOUNT,
    O.NET_AMOUNT,
    O.CURRENCY_CODE
COMMENT IS 'Order level reporting view used by BI tools';

CREATE OR REPLACE VIEW DEMO_FINANCE.OPEN_AR_V AS
SELECT
    I.INVOICE_ID,
    I.ORDER_ID,
    C.CUSTOMER_NAME,
    I.INVOICE_DATE,
    I.DUE_DATE,
    I.INVOICE_AMOUNT,
    I.PAID_AMOUNT,
    I.INVOICE_AMOUNT - I.PAID_AMOUNT AS OPEN_AMOUNT
FROM DEMO_FINANCE.INVOICES I
JOIN DEMO_SALES.CUSTOMERS C
  ON C.CUSTOMER_ID = I.CUSTOMER_ID
WHERE I.INVOICE_AMOUNT > I.PAID_AMOUNT
COMMENT IS 'Open accounts receivable items';

CREATE OR REPLACE VIEW DEMO_SUPPORT.CUSTOMER_360_V AS
SELECT
    C.CUSTOMER_ID,
    C.CUSTOMER_NAME,
    C.REGION,
    COUNT(DISTINCT O.ORDER_ID) AS ORDER_COUNT,
    COUNT(DISTINCT T.TICKET_ID) AS TICKET_COUNT,
    MAX(T.OPENED_AT) AS LAST_TICKET_OPENED_AT
FROM DEMO_SALES.CUSTOMERS C
LEFT JOIN DEMO_SALES.ORDERS O
  ON O.CUSTOMER_ID = C.CUSTOMER_ID
LEFT JOIN DEMO_SUPPORT.TICKETS T
  ON T.CUSTOMER_ID = C.CUSTOMER_ID
GROUP BY
    C.CUSTOMER_ID,
    C.CUSTOMER_NAME,
    C.REGION
COMMENT IS 'Customer summary spanning sales and support';

