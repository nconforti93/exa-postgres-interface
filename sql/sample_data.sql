CREATE OR REPLACE FUNCTION DEMO_SHARED.ORDER_SIZE_BAND (
    GROSS_AMOUNT DECIMAL(18, 2)
)
RETURN VARCHAR(20)
IS
    RESULT_BAND VARCHAR(20);
BEGIN
    IF GROSS_AMOUNT >= 10000 THEN
        RESULT_BAND := 'ENTERPRISE';
    ELSEIF GROSS_AMOUNT >= 5000 THEN
        RESULT_BAND := 'MIDMARKET';
    ELSE
        RESULT_BAND := 'SMB';
    END IF;
    RETURN RESULT_BAND;
END ORDER_SIZE_BAND;
