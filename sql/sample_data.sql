CREATE OR REPLACE SCRIPT DEMO_SHARED.UTILS AS
function normalize_priority(priority)
  if priority == nil then
    return 'UNKNOWN'
  end
  return string.upper(priority)
end
/

CREATE OR REPLACE LUA SCALAR SCRIPT DEMO_SHARED.PRIORITY_LABEL (
    PRIORITY VARCHAR(20),
    OWNER_TEAM VARCHAR(50)
) RETURNS VARCHAR(50) AS
function run(ctx)
  if ctx.PRIORITY == nil then
    return 'UNKNOWN'
  end
  local normalized = string.upper(ctx.PRIORITY)
  if normalized == 'CRITICAL' then
    return normalized .. '-IMMEDIATE'
  end
  if ctx.OWNER_TEAM == nil then
    return normalized
  end
  return normalized .. '-' .. string.upper(ctx.OWNER_TEAM)
end
/

